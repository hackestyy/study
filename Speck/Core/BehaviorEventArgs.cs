using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ZteApp.ProductService.Core
{
    public class BehaviorEventArgs:EventArgs
    {
        private Behavior mBehavior;

        public BehaviorEventArgs(Behavior behavior)
        {
            mBehavior = behavior;
        }

        public Behavior Behavior
        {
            get
            {
                return mBehavior;
            }
            set
            {
                mBehavior = value;
            }
        }
    }
}
